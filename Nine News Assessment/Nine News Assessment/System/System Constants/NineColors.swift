import UIKit

extension UIColor {
    static var headlineBlack: UIColor {
        return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1)
    }
    
    static var abstractBlack: UIColor {
        return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1)
    }
    
    static var lightGray: UIColor {
        return #colorLiteral(red: 0.938134253, green: 0.9331678152, blue: 0.9332562089, alpha: 1)
    }
}
